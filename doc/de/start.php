# WebFrap Web Frame Application Platform

## Übersicht über die Web Frame Application Plattform und ToolSuite

WebFrap ist eine Frame Applikation. Das bedeutet die WebFrap Platform ist bereits
eine fertige Applikation jedoch in der Basisform ohne Anwendungsfunktionalität.

Die Business Funktionalität, wie z.B. CMS, Shop, Abrechnungssysteme usw. werden
über Module implementiert und eingebunden.
Anwendungsfälle die bisher eher in isolierten Insellösungen implementiert wurden,
werden so einfach durch das hinzufügen eines vollintegrierten Moduls zur Gesammtlösung
umgesetzt.


### Ziele

- Hohe Interoperabilität der Module
- Hohe Wiederverwendbarkeit von Module
- Einfache Skalierbarkeit
- Starke Standartisierung für eine besser Automatisierbarkeit von Entwicklungsprozessen
- Sichere Infrastruktur




